mood= input ("What mood are you in?")

if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Cheer up!")
elif mood == "excited":
    print("Glad to see you are excited!")
elif mood == "relaxed":
    print ("How come? Your exam is tomorrow.")
else:
    print ("Mood not recognized.")

